package javaprogram;

public class Average 
{
	public static void main(String args[])
	{
		int p=56; int c=65; int b =85;
		int s;
		float avg;
		
		s=(p+c+b);
		avg=s/3;
		System.out.println("The total sum of the marks="+s);
		System.out.println("The average of the total marks obtained="+avg);
	}

}
